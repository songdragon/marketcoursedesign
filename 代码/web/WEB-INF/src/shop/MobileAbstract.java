package shop;

public abstract class MobileAbstract extends Products{
	public String brand;
	public String screen;
	public String nettype;
	public String appearance;
	public String screencolor;
	public String datatransport;
	public String camera;
	public String ontime;
	public String mptype;
	public String screentype;
	public String storage;
	public String color;
	
	public void setBrand(String brand){
		this.brand =brand;
	}
	public String getBrand(){
		return brand;
	}
	public void setScreen(String screen){
		this.screen =screen;
	}
	public String getScreen(){
		return screen;
	}
	public void setNettype(String nettype){
		this.nettype =nettype;
	}
	public String getNettype(){
		return nettype;
	}
	public void setAppearance(String appearance){
		this.appearance=appearance;
	}
	public String getAppearance(){
		return appearance;
	}
	public void setScreencolor(String screencolor){
		this.screencolor =screencolor;
	}
	public String getScreencolor(){
		return screencolor;
	}
	public void setDatatransport(String datatransport){
		this.datatransport=datatransport;
	}
	public String getDatatransport(){
		return datatransport;
	}
	public void setCamera(String camera){
		this.camera=camera;
	}
	public String getCamera(){
		return camera;
	}
	public void setOntime(String ontime){
		this.ontime=ontime;
	}
	public String getOntime(){
		return ontime;
	}
	public void setMptype(String mptype){
		this.mptype =mptype;
	}
	public String getMptype(){
		return mptype;
	}
	public void setScreentype(String screentype){
		this.screentype =screentype;
	}
	public String getScreentype(){
		return screentype;
	}
	public void setStorage(String storage){
		this.storage =storage;
	}
	public String getStorage(){
		return storage;
	}
	public void setColor(String color){
		this.color=color;
	}
	public String getColor(){
		return color;
	}
	
	
}
